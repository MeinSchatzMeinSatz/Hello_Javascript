/*
1. 빌트인 Number 오브젝트는 
- Number 처리를 위한 프로퍼티의 집합입니다.
- 즉, Number 처리를 위한 오브젝트 입니다.

2. JS에서 Number 처리를 위한 프로퍼티를
- 사전에 만들어 제공하므로
- 즉, built-in 으로 제공하므로
  - Number.length로 1을 구할 수 있으며
  - Number.isNaN()으로 함수를 호출할 수 있습니다.
*/

/*
빌트인 오브젝트의 유형(11개)
- Number 오브젝트
  - 123과 같은 숫자, 상수, 지수
- String 오브젝트
  -"abc"와 같은 문자열, 분리, 연결
- Boolean 오브젝트
  - true, false
- Object 오브젝트
  - {key: value} 형태
  - 키 밸류 형태의 프로퍼티를 지원합니다.
- Array 오브젝트
  - [1, 2, "A", "가나다"] 형태
- Function 오브젝트
  - function abc() {} 형태
- Math 오브젝트
  - abs(), round() 등의 수학 계산
- Date 오브젝트
  - 연월일, 시분초
- JSON 오브젝트
  - [{"name": "value"}] 형태
  - 서버와 데이터 송수신에 사용
- RegExp 오브젝트
  - ^$와 같은 정규 표현식
  - 본 강좌에서 다루지 않음
- Global 오브젝트
  - 소스 파일 전체에서 하나만 존재
  - 모든 코드에서 공유, 접근 가능
  - 전역 객체라고도 하며, 뉘앙스에는 차이가 있다.
*/
